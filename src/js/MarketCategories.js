    render()
    {
        // render groups
        this.categories.forEach((categories, title) => {
            this.ui.append(`<h3>${title}</h3>`);

            let html = [];
            categories.forEach((category, i) => {
                html.push(`<button id="${category.ID}"><img src="${Icon.get(category.Icon)}"><span>${category.Name}</span></button>`);
            });

            this.ui.append(`<div>${html.join('')}</div>`);
        });


        // watch for selection
        this.watchForSelection();
    }

    watchForSelection()
    {
        this.ui.on('click', 'button', event => {
            const categoryItem = $(event.currentTarget).attr('id');

            // hide "home"
            this.home.removeClass('on');

            // move to top
            window.scrollTo(0,0);

            // load market stock for this category
            MarketCategoryStock.listCategoryStock(categoryItem);

            // add visual "on"
            this.ui.find('button.on').removeClass('on');
            $(event.currentTarget).addClass('on');
        });
    }
